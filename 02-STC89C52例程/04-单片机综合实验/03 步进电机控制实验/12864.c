/*-------------------------------------------------------
函数名称: Write_Com()
函数功能: 向LCD12864里写命令
参数    : left  左边芯片选通位
right 右边芯片选通位
Com  要写的命令
返回值  : 无
---------------------------------------------------------*/
void Write_Com(uchar com)
{

	LCD_E=0;
	LCD_RS=0;
	LCD_E=1; 
	Data_Port=com;  
}
/*-------------------------------------------------------
函数名称: Write_Data()
函数功能: 向LCD12864里写数据
参数    : left  左边芯片选通位
right 右边芯片选通位
Data  要写的数据
返回值  : 无
---------------------------------------------------------*/
void Write_Data(uchar Data)
{

	LCD_E=0;
	LCD_RS=1;
	LCD_E=1;
	Data_Port=Data;  
}
/*-----------------------------------------
函数名称: LCD12864_Disp()
函数功能: 在任意位置上显示汉字
参数    : X X地址位置 
Y Y地址位置 
*P 指针 数据的首地址
返回值  : 无
------------------------------------------*/
void LCD12864_Disp(uchar x, uchar y, uchar *p )
{
	uchar i,j;
	char k;

	for(i=0;i<32;i++)
	{
		if(i>=16)
		{
			j=1;
			k=-16;
		}
		else j=0,k=0;
		Write_Com(0xb8+j+x);     //设置X地址
		Write_Com(0x40+i+y+k);   //设置Y地址
		Write_Data(p[i]);        //送数据
	}

}
/*-----------------------------------------
函数名称: LCD12864_DispNum()
函数功能: 在任意位置上显示数字
参数    : X X地址位置 
Y Y地址位置 
*P 指针 数据的首地址
返回值  : 无
------------------------------------------*/
void LCD12864_DispNum(uchar x, uchar y, uchar *p )
{
	uchar i,j;
	char k;
	for(i=0;i<16;i++)
	{    
		if(i>=8)
		{
		j=1;
		k=-8;
		}
		else j=0,k=0;
		Write_Com(0xb8+x+j); //设置X地址
		Write_Com(0x40+i+y+k);//设置Y地址
		Write_Data(p[i]);//送数据
	}
}
/*-------------------------------------------------------
函数名称: LCD12864_Init()
函数功能: LCD12864初始化
参数    : 
返回值  : 无
---------------------------------------------------------*/
void LCD12864_Init()
{
	Data_Port=0xFF;
	LCD_CS1=0;
	LCD_CS2=0;
	LCD_E=1;
	LCD_RW=0;
	Write_Com(0x3e);//显示关
	Write_Com(0x3f);//显示开
	Write_Com(0xc0);//显示起始行
	Write_Com(0xb8);//设置X地址
	Write_Com(0x40);//设置Y地址	、
	

}
/*---------------------------------------------------
函数名称: LCD12864_Clear()
函数功能: 清屏
参数    : left  清左屏
right 清右屏
返回值  ：无
【说明】 : 0 0  不清屏  11  清全屏
0 1  清右屏  10  清左屏
-----------------------------------------------------*/
void LCD12864_Clear()
{
	uchar aa,bb;//aa控制X地址 bb控制Y地址

		for(aa=0;aa<8;aa++)
		{
			Write_Com(0xb8+aa);
			LCD_CS1=0;
			LCD_CS2=1;
			for(bb=0;bb<64;bb++)
			{
				Write_Data(0x00);
			}
			LCD_CS1=1;
			LCD_CS2=0;
			for(bb=0;bb<64;bb++)
			{

				Write_Data(0x00);
			} 
		}
 
}
