% 定义无限长序列的采样周期和长度
Ts = 0.2; % 采样周期
N1 = 8; % 序列长度为8
N2 = 32; % 序列长度为32
N3 = 64; % 序列长度为64

% 计算时间序列和无限长序列的采样值
t1 = (0:N1-1) * Ts; % 时间序列（长度为N1）
x1 = 0.5 * t1; % 无限长序列的采样值

t2 = (0:N2-1) * Ts; % 时间序列（长度为N2）
x2 = 0.5 * t2; % 无限长序列的采样值

t3 = (0:N3-1) * Ts; % 时间序列（长度为N3）
x3 = 0.5 * t3; % 无限长序列的采样值

% 用FFT计算频谱
X1 = fft(x1, N1);
X2 = fft(x2, N2);
X3 = fft(x3, N3);

% 绘制频谱
figure;
subplot(3, 1, 1);
stem(0:N1-1, abs(X1));
xlabel('频率');
ylabel('幅度');
title('N=8时的频谱');

subplot(3, 1, 2);
stem(0:N2-1, abs(X2));
xlabel('频率');
ylabel('幅度');
title('N=32时的频谱');

subplot(3, 1, 3);
stem(0:N3-1, abs(X3));
xlabel('频率');
ylabel('幅度');
title('N=64时的频谱');